# ============ 执行 ============
point_sqls = []
update_sqls = []

for idx, (rid, name, city, diff, exp_m, waypoints_gcj) in enumerate(ROUTES):
    # GCJ-02 → WGS-84
    waypoints_wgs = [gcj02_to_wgs84(lat, lng) for lat, lng in waypoints_gcj]
    coords_str = ";".join(["{:.7f},{:.7f}".format(lng, lat) for lat, lng in waypoints_wgs])

    url = f"https://router.project-osrm.org/route/v1/foot/{coords_str}"
    params = {"overview": "full", "geometries": "geojson", "steps": "false", "continue_straight": "false"}

    status = ""
    try:
        resp = requests.get(url, params=params, timeout=25)
        data = resp.json()
        if data["code"] != "Ok":
            status = "FAIL: " + data.get("message", data["code"])
            print(f"[{idx+1}/28] {name}: {status}")
            time.sleep(1.5)
            continue

        rd = data["routes"][0]
        wgs_coords = rd["geometry"]["coordinates"]
        dist_m = rd["distance"]

        # 简化（每5取1）
        pts = []
        for i, pt in enumerate(wgs_coords):
            if i % 5 == 0 or i == len(wgs_coords) - 1:
                gcj_lat, gcj_lng = wgs84_to_gcj02(pt[1], pt[0])
                pts.append((gcj_lat, gcj_lng))

        center_lat = sum(p[0] for p in pts) / len(pts)
        center_lng = sum(p[1] for p in pts) / len(pts)
        slat, slng = pts[0]
        dev = abs(dist_m - exp_m) / exp_m * 100

        print(f"[{idx+1}/28] {name}: {dist_m/1000:.1f}km, {len(pts)}pts, 偏差{dev:.0f}% {'YIKES' if dev>30 else 'OK'}")

        for i, (glat, glng) in enumerate(pts):
            point_sqls.append(f"('{rid}',{i},{glat:.7f},{glng:.7f},0)")

        update_sqls.append(
            f"UPDATE routes SET distance={dist_m/1000:.2f}, "
            f"start_lat={slat:.7f}, start_lng={slng:.7f}, "
            f"center_lat={center_lat:.7f}, center_lng={center_lng:.7f}, "
            f"city='{city}', difficulty={diff} WHERE id='{rid}';"
        )

    except Exception as e:
        print(f"[{idx+1}/28] {name}: ERROR - {str(e)[:60]}")
        time.sleep(1.5)
        continue

    time.sleep(1.2)

# 组装 SQL
