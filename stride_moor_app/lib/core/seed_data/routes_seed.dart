/// 生成平滑路径：路标点 + 余弦插值 + 横向摇摆 + GPS 噪声
List<Map<String, double>> _smoothPath(
  List<List<double>> waypoints, {
  int pointsPerSegment = 25,
  double wobbleAmp = 0.000025,
  double wobbleFreq = 2.5,
  double noiseAmp = 0.00003,
}) {
  final random = Random(42);
  final totalPoints = (waypoints.length - 1) * pointsPerSegment;
  final lats = <double>[];
  final lngs = <double>[];

  for (int seg = 0; seg < waypoints.length - 1; seg++) {
    final lat0 = waypoints[seg][0], lat1 = waypoints[seg + 1][0];
    final lng0 = waypoints[seg][1], lng1 = waypoints[seg + 1][1];
    for (int i = 0; i < pointsPerSegment; i++) {
      final t = i / pointsPerSegment;
      final ct = (1 - cos(t * pi)) / 2;
      lats.add(lat0 + (lat1 - lat0) * ct);
      lngs.add(lng0 + (lng1 - lng0) * ct);
    }
  }

  for (int i = 0; i < totalPoints; i++) {
    final t = i / totalPoints;
    final wobble = wobbleAmp * sin(2 * pi * wobbleFreq * t + 1.2);
    lats[i] += wobble + (random.nextDouble() - 0.5) * noiseAmp;
    lngs[i] += wobble * 0.5 + (random.nextDouble() - 0.5) * noiseAmp;
  }

  return List.generate(totalPoints, (i) => {'lat': lats[i], 'lng': lngs[i]});
}

