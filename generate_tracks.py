# ============ GCJ-02 工具 ============
def wgs84_to_gcj02(wgs_lat, wgs_lng):
    a, ee = 6378245.0, 0.00669342162296594323
    x, y = wgs_lng - 105.0, wgs_lat - 35.0
    dLon = 300.0 + x + 2.0*y + 0.1*x*x + 0.1*x*y + 0.1*math.sqrt(abs(x))
    dLon += (20.0*math.sin(6.0*x*math.pi) + 20.0*math.sin(2.0*x*math.pi))*2.0/3.0
    dLon += (20.0*math.sin(x*math.pi) + 40.0*math.sin(x/3.0*math.pi))*2.0/3.0
    dLon += (150.0*math.sin(x/12.0*math.pi) + 300.0*math.sin(x/30.0*math.pi))*2.0/3.0
    dLat = -100.0 + 2.0*x + 3.0*y + 0.2*y*y + 0.1*x*y + 0.2*math.sqrt(abs(x))
    dLat += (20.0*math.sin(6.0*x*math.pi) + 20.0*math.sin(2.0*x*math.pi))*2.0/3.0
    dLat += (20.0*math.sin(y*math.pi) + 40.0*math.sin(y/3.0*math.pi))*2.0/3.0
    dLon += (160.0*math.sin(y/12.0*math.pi) + 320.0*math.sin(y*math.pi/30.0))*2.0/3.0
    rad_lat = wgs_lat/180.0*math.pi
    magic = math.sin(rad_lat); magic = 1 - ee*magic*magic
    sqrt_magic = math.sqrt(magic)
    dLat = (dLat*180.0)/((a*(1-ee))/(magic*sqrt_magic)*math.pi)
    dLon = (dLon*180.0)/(a/sqrt_magic*math.cos(rad_lat)*math.pi)
    return wgs_lat+dLat, wgs_lng+dLon

def haversine(lat1, lng1, lat2, lng2):
    R = 6371000
    dlat = (lat2-lat1)*math.pi/180
    dlng = (lng2-lng1)*math.pi/180
    a = math.sin(dlat/2)**2 + math.cos(lat1*math.pi/180)*math.cos(lat2*math.pi/180)*math.sin(dlng/2)**2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1-a))

def densify(waypoints_gcj, gap_m=250):
    """加密路径点，每gap_m米加一个点"""
    result = [waypoints_gcj[0]]
    for i in range(len(waypoints_gcj)-1):
        lat1, lng1 = waypoints_gcj[i]
        lat2, lng2 = waypoints_gcj[i+1]
        seg_dist = haversine(lat1, lng1, lat2, lng2)
        steps = max(1, int(seg_dist / gap_m))
        for j in range(1, steps+1):
            t = j / (steps+1)
            result.append((lat1 + (lat2-lat1)*t, lng1 + (lng2-lng1)*t))
    # 最后加回终点
    if result[-1] != waypoints_gcj[-1]:
        result.append(waypoints_gcj[-1])
    return result

def interpolate_pts(waypoints_gcj, target_dist_m, point_interval=10):
    """生成接近 target_dist_m 的GPS轨迹"""
    # 先加密
    dense = densify(waypoints_gcj, gap_m=250)
    
    # 计算当前直连距离
    raw_dist = 0
    for i in range(1, len(dense)):
        raw_dist += haversine(dense[i-1][0], dense[i-1][1], dense[i][0], dense[i][1])
    
    # 如果需要更长距离，把路径来回跑/延伸
    if raw_dist < target_dist_m * 0.7:
        factor = max(1, int(target_dist_m * 1.2 / raw_dist))
        # 多次往返增加距离
        expanded = []
        for lap in range(factor):
            pts = dense if lap % 2 == 0 else list(reversed(dense))
            for p in pts:
                expanded.append(p)
        dense = expanded
    
    # 计算调整后的距离
    total_raw = 0
    for i in range(1, len(dense)):
        total_raw += haversine(dense[i-1][0], dense[i-1][1], dense[i][0], dense[i][1])
    
    # 按目标间距采样
    n_pts = max(20, int(total_raw / point_interval))
    if n_pts > len(dense):
        n_pts = len(dense)
    
    idxs = [int(i * (len(dense)-1) / (n_pts-1)) for i in range(n_pts)]
    
    pts = []
    for idx in idxs:
        lat, lng = dense[idx]
        # GPS抖动
        noise_lat = random.uniform(-0.00003, 0.00003)
        noise_lng = random.uniform(-0.00003, 0.00003)
        pts.append((lat + noise_lat, lng + noise_lng))
    
    # 计算实际距离（平滑后）
    actual_dist = 0
    for i in range(1, len(pts)):
        actual_dist += haversine(pts[i-1][0], pts[i-1][1], pts[i][0], pts[i][1])
    
    return pts, actual_dist

