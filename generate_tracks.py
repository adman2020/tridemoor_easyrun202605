# ============ 生成 ============
point_sqls = []
update_sqls = []
tags_sqls = []

total_ok = 0
total_yikes = 0

for idx, (rid, name, city, diff, target_m, tags_str, waypoints_gcj) in enumerate(ROUTES):
    pts, actual_dist = interpolate_pts(waypoints_gcj, target_m, point_interval=12)

    slat, slng = pts[0]
    center_lat = sum(p[0] for p in pts) / len(pts)
    center_lng = sum(p[1] for p in pts) / len(pts)
    dev = abs(actual_dist - target_m) / target_m * 100

    label = "OK" if dev < 15 else ("~" if dev < 30 else "YIKES")
    if label == "YIKES": total_yikes += 1
    else: total_ok += 1

    print(f"[{idx+1}/28] {name}: {actual_dist/1000:.2f}km, {len(pts)}pts ({label}, 偏差{dev:.0f}%)")

    # 生成点SQL
    for i, (glat, glng) in enumerate(pts):
        point_sqls.append(f"('{rid}',{i},{glat:.7f},{glng:.7f},0)")

    # 路线更新
    update_sqls.append(
        f"UPDATE routes SET distance={actual_dist/1000:.2f}, "
        f"start_lat={slat:.7f}, start_lng={slng:.7f}, "
        f"center_lat={center_lat:.7f}, center_lng={center_lng:.7f}, "
        f"city='{city}', difficulty={diff}, tags='{tags_str}' "
        f"WHERE id='{rid}';"
    )

# 组装SQL
